"""Roll stabilization (v52.0).

Стабилизация крена двумя независимыми группами исполнительных механизмов:

1) ГОРИЗОНТАЛЬНЫЕ РУЛИ (4 шт: кормовые hl/hr + носовые hfl/hfr)
   Дифференциальное отклонение левого и правого создаёт момент крена:
       hl = depth - roll_h      hr = depth + roll_h
   (носовые зеркальны кормовым, см. autopilot_node._pub)
   Это уже «вшито» в DepthRudderController, здесь мы вычисляем общий
   дифференциальный терм roll_h, чтобы все рули жили по единому закону.

2) ВЕРХНИЙ ВЕРТИКАЛЬНЫЙ РУЛЬ (vertical_rudder_top, ось X, на корме сверху)
   Поскольку он смещён ВВЕРХ от продольной оси, его боковая сила даёт
   чистый момент крена. Канал отдельный и НЕ инвертируется относительно
   нижнего вертикального руля (нижний рулит курсом, верхний — креном).

Знак: roll > 0 (крен на правый борт) → нужен восстанавливающий момент
в обратную сторону.
"""
from .models import VehicleState, PID as P, Lim as L


class RollController:
    """Единый регулятор крена для горизонтальных рулей и верхнего верт. руля."""

    def __init__(self):
        self._rvt = 0.0   # текущее положение верхнего вертикального руля

    @staticmethod
    def _sl(c, t, r, dt):
        d = r * dt
        return c + max(-d, min(d, t - c))

    def reset(self):
        self._rvt = 0.0

    # ---- дифференциальный терм для ГОРИЗОНТАЛЬНЫХ рулей ----
    @staticmethod
    def horizontal_term(s: VehicleState) -> float:
        """Возвращает roll_h: добавляется к правому рулю и вычитается из левого.
        Приоритет крена снижается, когда велика ошибка по глубине."""
        rp = P.Kp_roll * s.rpy[0] + P.Kd_roll * s.roll_d
        rp *= max(0.15, 1.0 - (abs(s.z_err) / 5.0))
        return rp

    # ---- отдельный канал ВЕРХНЕГО вертикального руля ----
    def vertical_top(self, s: VehicleState, phase: str, dt: float) -> float:
        """Возвращает положение верхнего вертикального руля для гашения крена.
        НЕ инвертируется. Активен во всех фазах (даже на зависании)."""
        # PD-регулятор по крену
        tgt = P.Kp_roll_v * s.rpy[0] + P.Kd_roll_v * s.roll_d
        tgt = max(-P.roll_v_lim, min(P.roll_v_lim, tgt))
        self._rvt = self._sl(self._rvt, tgt, L.rud_spd, dt)
        return self._rvt
